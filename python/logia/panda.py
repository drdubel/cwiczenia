from turtle import *

a = 10


def wielokat(dl, katy):
    for i in range(katy):
        fd(dl * a)
        lt(360 / katy)


def glowa():
    wielokat(8, 12)


def uszy():
    lt(150)
    for i in range(3):
        fd(8 * a)
        rt(30)
    fd(2 * a)
    begin_fill()
    wielokat(4, 8)
    end_fill()
    fd(6 * a)
    for i in range(3):
        rt(30)
        fd(8 * a)
    rt(30)
    fd(2 * a)
    begin_fill()
    wielokat(4, 8)
    end_fill()


def oczy():
    pu()
